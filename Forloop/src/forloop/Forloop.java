
package forloop;


public class Forloop {

    
    public static void main(String[] args) {
        int i,j;
        for(i=65;i<=69;i++)
        {
            System.out.println(i);
        }
        for(j=65;j<69;j++)
        {
            System.out.println(j);
        }
    }
    
}
