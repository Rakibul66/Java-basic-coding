package mathjava;


public class MathJava {

  
    public static void main(String[] args) {
        System.out.println(Math.pow(6,3));
        System.out.println(Math.ceil(7.5));
        System.out.println(Math.floor(7.5));
        System.out.println(Math.max(7.5,8.5));
        System.out.println(Math.min(7.5,8.5));
        System.out.println(Math.sqrt(9));
        
    }
    
    
}
