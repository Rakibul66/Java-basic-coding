package while_loop;

public class While_loop {

 
    public static void main(String[] args) {
        int n=5;
        int m=4;
        while(n>0){
            System.out.println(+n);
            n--;
        }
        while(m>0){
            System.out.println(+m);
            m--;
        }
    }
    
}
